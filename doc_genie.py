def export_to_pdf(updated_code: str, updates: List[Dict[str, Any]]) -> str:
    fd, path = tempfile.mkstemp(suffix=".pdf")
    os.close(fd)
    doc = SimpleDocTemplate(path, pagesize=A4)
    story = []

    title_style = ParagraphStyle(name="Title", fontName="Helvetica-Bold", fontSize=16, spaceAfter=12, alignment=TA_LEFT)
    header_style = ParagraphStyle(name="Header", fontName="Helvetica-Bold", fontSize=12, spaceAfter=8)
    body_style = ParagraphStyle(name="Body", fontName="Helvetica", fontSize=10, leading=13)

    story.append(Paragraph("Doc-Genie — Automated Docstring Report", title_style))
    story.append(Paragraph(datetime.now().strftime("%Y-%m-%d %H:%M:%S"), body_style))
    story.append(Spacer(1, 10))

    data = [["Function", "Action", "Style", "Loops", "Conds", "Ops", "Calls"]]
    for u in updates:
        a = u["analysis"]
        data.append([
            u["function"],
            u["action"],
            u["style"],
            "✓" if a["has_loops"] else "—",
            "✓" if a["has_conditions"] else "—",
            ", ".join(a["operations"]) or "—",
            ", ".join(a["function_calls"]) or "—",
        ])
    table = Table(data, hAlign="LEFT")
    table.setStyle(TableStyle([
        ("BACKGROUND", (0,0), (-1,0), colors.lightgrey),
        ("GRID", (0,0), (-1,-1), 0.5, colors.grey),
        ("FONTNAME", (0,0), (-1,0), "Helvetica-Bold")
    ]))
    story.append(table)
    story.append(Spacer(1, 12))
    story.append(Paragraph("Updated Code", header_style))
    story.append(Spacer(1, 4))
    story.append(Preformatted(updated_code, style=body_style))
    doc.build(story)
    return path

def export_to_txt(updated_code: str, updates: List[Dict[str, Any]]) -> str:
    fd, path = tempfile.mkstemp(suffix=".txt")
    with os.fdopen(fd, "w", encoding="utf-8") as f:
        f.write("# Doc-Genie — Documentation Export\n")
        f.write(datetime.now().strftime("%Y-%m-%d %H:%M:%S") + "\n\n")
        f.write("## Summary\n")
        for u in updates:
            a = u["analysis"]
            f.write(f"- {u['function']} [{u['action']}, style={u['style']}]; loops={a['has_loops']}; conds={a['has_conditions']}; ops={','.join(a['operations']) or '-'}; calls={','.join(a['function_calls']) or '-'}\n")
        f.write("\n## Updated Code\n\n")
        f.write(updated_code)
    return path

