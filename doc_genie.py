with gr.Blocks(title="Doc-Genie — AST Docstring Generator") as demo:
    gr.Markdown("## Doc-Genie — Professional Python Function Docstring Generator\n"
                "Paste code or upload a `.py` file, choose a docstring style, and generate. "
                "Then export to PDF/TXT or share.")
    with gr.Row():
        with gr.Column(scale=3):
            code_in = gr.Code(label="Python code input (.py)", value=EXAMPLE_CODE, language="python", lines=20)
            file_in = gr.File(label="Upload a .py file (optional)", file_types=[".py"])
            style = gr.Radio(["google", "numpy"], value="google", label="Docstring style")
            generate_btn = gr.Button("Generate Docstrings", variant="primary")
        with gr.Column(scale=2):
            updated_code = gr.Code(label="Code with generated docstrings", language="python", lines=20)
            analysis_json = gr.JSON(label="Analysis summary (per function)")
            with gr.Row():
                pdf_btn = gr.Button("Export PDF")
                txt_btn = gr.Button("Export TXT")
            pdf_out = gr.File(label="PDF file")
            txt_out = gr.File(label="TXT file")
            share_md = gr.Markdown()

    history_state = gr.State([])

    def _read_input(code_text, file_obj):
        # Gradio 4.x file payloads vary: could be path string, dict with 'path', or file object.
        if file_obj:
            try:
                # If dict-like with a 'path' key
                if isinstance(file_obj, dict) and "path" in file_obj:
                    path = file_obj["path"]
                # If it's a list (multi-file), take the first
                elif isinstance(file_obj, list) and file_obj:
                    first = file_obj[0]
                    if isinstance(first, dict) and "path" in first:
                        path = first["path"]
                    else:
                        path = getattr(first, "name", str(first))
                else:
                    path = getattr(file_obj, "name", str(file_obj))
                with open(path, "r", encoding="utf-8") as fh:
                    return fh.read()
            except Exception:
                pass
        return code_text or ""

    def do_generate(code_text, file_obj, style_sel, history):
        code = _read_input(code_text, file_obj).replace("\r\n", "\n")
        if not code.strip():
            return gr.update(), gr.update(), history, gr.update(value="> Please provide Python code.")
        updated, updates = DocGenieAnalyzer.inject_docstrings(code, style_sel)
        ts = datetime.now().isoformat(timespec="seconds")
        history = (history or []) + [{"timestamp": ts, "updates": updates}]
        msg = f"Doc-Genie generated docstrings for {len(updates)} function(s) at {ts}."
        wa = f"https://api.whatsapp.com/send?text={quote(msg)}"
        fb = f"https://www.facebook.com/sharer/sharer.php?u=&quote={quote(msg)}"
        share_block = f"**Share:** [WhatsApp]({wa}) · [Facebook]({fb})"
        return updated, updates, history, gr.update(value=share_block)

    def do_pdf(updated, updates):
        if not updated:
            return None
        path = export_to_pdf(updated, updates or [])
        return path

    def do_txt(updated, updates):
        if not updated:
            return None
        path = export_to_txt(updated, updates or [])
        return path

    generate_btn.click(
        do_generate,
        inputs=[code_in, file_in, style, history_state],
        outputs=[updated_code, analysis_json, history_state, share_md]
    )
    pdf_btn.click(do_pdf, inputs=[updated_code, analysis_json], outputs=[pdf_out])
    txt_btn.click(do_txt, inputs=[updated_code, analysis_json], outputs=[txt_out])

