def _unparse(node: ast.AST) -> str:
    """Return Python source for an AST node (requires Python 3.9+)."""
    try:
        return ast.unparse(node)
    except Exception:
        return repr(node)

def _annotation_to_text(ann) -> Optional[str]:
    if ann is None:
        return None
    try:
        return _unparse(ann)
    except Exception:
        return None

def _default_to_text(node) -> Optional[str]:
    if node is None:
        return None
    try:
        return _unparse(node)
    except Exception:
        return None

def _guess_literal_type(node: ast.AST) -> Optional[str]:
    if isinstance(node, ast.Constant):
        if isinstance(node.value, bool):
            return "bool"
        if isinstance(node.value, int):
            return "int"
        if isinstance(node.value, float):
            return "float"
        if isinstance(node.value, str):
            return "str"
        if node.value is None:
            return "None"
    if isinstance(node, (ast.List, ast.Tuple, ast.Set)):
        return "Sequence"
    if isinstance(node, ast.Dict):
        return "dict"
    return None

def _union_type_str(types: List[str]) -> str:
    uniq = sorted({t for t in types if t})
    if not uniq:
        return "Any"
    if len(uniq) == 1:
        return uniq[0]
    return f"Union[{', '.join(uniq)}]"

