class DocGenieAnalyzer:
    """
    Core AST-based analyzer that extracts function signatures and performs
    lightweight logic analysis to inform docstring generation.
    """

    @staticmethod
    def parse_functions(code: str) -> List[ast.AST]:
        tree = ast.parse(code)
        return [n for n in ast.walk(tree) if isinstance(n, (ast.FunctionDef, ast.AsyncFunctionDef))]

    @staticmethod
    def extract_function_signature(func_def: ast.AST) -> Dict[str, Any]:
        args = func_def.args
        params: List[Dict[str, Any]] = []

        posonly = getattr(args, "posonlyargs", [])
        all_positional = list(posonly) + list(args.args)
        defaults = [None] * (len(all_positional) - len(args.defaults)) + list(args.defaults)

        for arg, default in zip(all_positional, defaults):
            params.append({
                "name": arg.arg,
                "type": _annotation_to_text(arg.annotation) or "Any",
                "default": _default_to_text(default)
            })

        if args.vararg:
            params.append({
                "name": f"*{args.vararg.arg}",
                "type": _annotation_to_text(args.vararg.annotation) or "Any",
                "default": None
            })

        for kw, default in zip(args.kwonlyargs, args.kw_defaults):
            params.append({
                "name": kw.arg,
                "type": _annotation_to_text(kw.annotation) or "Any",
                "default": _default_to_text(default)
            })

        if args.kwarg:
            params.append({
                "name": f"**{args.kwarg.arg}",
                "type": _annotation_to_text(args.kwarg.annotation) or "Any",
                "default": None
            })

        return_type = _annotation_to_text(func_def.returns) or "Any"
        return {
            "name": func_def.name,
            "params": params,
            "return_type": return_type,
            "is_async": isinstance(func_def, ast.AsyncFunctionDef),
            "decorators": [_unparse(d) for d in getattr(func_def, "decorator_list", [])],
        }

    @staticmethod
    def analyze_function_logic(func_def: ast.AST) -> Dict[str, Any]:
        ops_map = {
            ast.Add: "addition",
            ast.Sub: "subtraction",
            ast.Mult: "multiplication",
            ast.Div: "division",
            ast.FloorDiv: "floor_division",
            ast.Mod: "modulo",
            ast.Pow: "power",
            ast.MatMult: "matrix_multiplication",
        }

        has_loops = False
        has_conditions = False
        operations: List[str] = []
        function_calls: List[str] = []
        variables: List[str] = []
        returns_types: List[str] = []

        class V(ast.NodeVisitor):
            def visit_For(self, node):
                nonlocal has_loops
                has_loops = True
                self.generic_visit(node)
            def visit_While(self, node):
                nonlocal has_loops
                has_loops = True
                self.generic_visit(node)
            def visit_If(self, node):
                nonlocal has_conditions
                has_conditions = True
                self.generic_visit(node)
            def visit_BinOp(self, node):
                op_type = ops_map.get(type(node.op))
                if op_type:
                    operations.append(op_type)
                self.generic_visit(node)
            def visit_Call(self, node):
                name = None
                if isinstance(node.func, ast.Name):
                    name = node.func.id
                elif isinstance(node.func, ast.Attribute):
                    name = f"{_unparse(node.func.value)}.{node.func.attr}"
                if name:
                    function_calls.append(name)
                self.generic_visit(node)
            def visit_Name(self, node):
                variables.append(node.id)
                self.generic_visit(node)
            def visit_Return(self, node):
                if node.value is None:
                    returns_types.append("None")
                else:
                    guess = _guess_literal_type(node.value)
                    returns_types.append(guess or "Unknown")
                self.generic_visit(node)

        V().visit(func_def)
        operations = sorted(set(operations))
        function_calls = sorted(set(function_calls))
        variables = sorted(set(variables))
        returns_types = sorted(set([t for t in returns_types if t]))

        desc_parts = []
        if has_loops:
            desc_parts.append("iterative processing")
        if has_conditions:
            desc_parts.append("conditional logic")
        if operations:
            desc_parts.append("mathematical operations: " + ", ".join(operations))
        if function_calls:
            desc_parts.append("calls: " + ", ".join(function_calls))
        description = "Executes " + (", ".join(desc_parts) if desc_parts else "core computation") + "."

        return {
            "has_loops": has_loops,
            "has_conditions": has_conditions,
            "operations": operations,
            "function_calls": function_calls,
            "variables": variables,
            "returns_types": returns_types or ["Unknown"],
            "description": description
        }

    @staticmethod
    def generate_google_docstring(signature: Dict[str, Any], analysis: Dict[str, Any]) -> str:
        name = signature["name"]
        summary = f"{name}.\n\n{analysis['description']}"
        args_lines = []
        for p in signature["params"]:
            pname = p["name"]
            ptype = p["type"] or "Any"
            if pname.startswith("*"):
                pname_disp = pname
            else:
                pname_disp = f"{pname} ({ptype})"
            args_lines.append(f"    {pname_disp}: Parameter `{pname}`.")
        returns = _union_type_str([signature["return_type"]] + analysis.get("returns_types", []))
        ds = '"""' + f"\n{summary}\n\n"
        if args_lines:
            ds += "Args:\n" + "\n".join(args_lines) + "\n\n"
        ds += f"Returns:\n    {returns}: Function result.\n" + '"""'
        return ds

    @staticmethod
    def generate_numpy_docstring(signature: Dict[str, Any], analysis: Dict[str, Any]) -> str:
        name = signature["name"]
        summary = f"{name}.\n\n{analysis['description']}"
        params_lines = []
        for p in signature["params"]:
            pname = p["name"]
            ptype = p["type"] or "Any"
            params_lines.append(f"{pname} : {ptype}\n    Parameter `{pname}`.")
        returns = _union_type_str([signature["return_type"]] + analysis.get("returns_types", []))
        ds = '"""' + f"\n{summary}\n\n"
        if params_lines:
            ds += "Parameters\n----------\n" + "\n".join(params_lines) + "\n\n"
        ds += "Returns\n-------\n" + f"{returns}\n    Function result.\n" + '"""'
        return ds

    @staticmethod
    def inject_docstrings(code: str, style: str = "google") -> Tuple[str, List[Dict[str, Any]]]:
        tree = ast.parse(code)
        updates: List[Dict[str, Any]] = []

        for node in ast.walk(tree):
            if not isinstance(node, (ast.FunctionDef, ast.AsyncFunctionDef)):
                continue

            sig = DocGenieAnalyzer.extract_function_signature(node)
            analysis = DocGenieAnalyzer.analyze_function_logic(node)
            if style.lower() == "numpy":
                docstring = DocGenieAnalyzer.generate_numpy_docstring(sig, analysis)
            else:
                docstring = DocGenieAnalyzer.generate_google_docstring(sig, analysis)

            if node.body and isinstance(node.body[0], ast.Expr) and isinstance(getattr(node.body[0], "value", None), ast.Constant) and isinstance(node.body[0].value.value, str):
                node.body[0].value.value = docstring
                action = "replaced"
            else:
                doc_node = ast.Expr(value=ast.Constant(value=docstring))
                node.body.insert(0, doc_node)
                action = "inserted"

            updates.append({
                "function": sig["name"],
                "action": action,
                "style": style.lower(),
                "analysis": analysis,
                "signature": sig
            })

        ast.fix_missing_locations(tree)
        updated = _unparse(tree)
        return updated, updates

